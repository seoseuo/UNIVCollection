package beans;

public class ROwnerBean {
	
	private String roID;
	private String roPW;
	private String roNAME;
	private String roMALL;
	private String roADDRESS;
	
	public String getRoID() {
		return roID;
	}
	public void setRoID(String roID) {
		this.roID = roID;
	}
	public String getRoPW() {
		return roPW;
	}
	public void setRoPW(String roPW) {
		this.roPW = roPW;
	}
	public String getRoNAME() {
		return roNAME;
	}
	public void setRoNAME(String roNAME) {
		this.roNAME = roNAME;
	}
	public String getRoMALL() {
		return roMALL;
	}
	public void setRoMALL(String roMALL) {
		this.roMALL = roMALL;
	}
	public String getRoADDRESS() {
		return roADDRESS;
	}
	public void setRoADDRESS(String roADDRESS) {
		this.roADDRESS = roADDRESS;
	}
	
	

}
